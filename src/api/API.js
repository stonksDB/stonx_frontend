function toggleTickerPreference(ticker, action) {
  let method = "";
  if (action === "add") method = "post";
  else method = "delete";

  return new Endpoint({
    path: "user/like",
    method: method,
    pathParam: { ticker: ticker },
    responseFormat: "",
  }).getResponse();
}

